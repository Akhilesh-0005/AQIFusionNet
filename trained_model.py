"""# Cell 16 — Rolling Window Features"""

def add_rolling_features(df, target_col, windows):
    df = df.copy()
    for w in windows:
        df[f"{target_col}_rollmean{w}"] = df[target_col].shift(1).rolling(window=w).mean()
        df[f"{target_col}_rollstd{w}"]  = df[target_col].shift(1).rolling(window=w).std()
        df[f"{target_col}_rollmax{w}"]  = df[target_col].shift(1).rolling(window=w).max()
        df[f"{target_col}_rollmin{w}"]  = df[target_col].shift(1).rolling(window=w).min()
    return df

