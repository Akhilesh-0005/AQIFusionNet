def evaluate_model(y_true, y_pred, model_name, train_time=None, inference_time=None):
    y_true = np.array(y_true).flatten()
    y_pred = np.array(y_pred).flatten()

    mae = mean_absolute_error(y_true, y_pred)
    mse = mean_squared_error(y_true, y_pred)
    rmse = np.sqrt(mse)
    # Avoid divide-by-zero in MAPE by excluding near-zero actuals
    mask = y_true > 1e-3
    mape = np.mean(np.abs((y_true[mask] - y_pred[mask]) / y_true[mask])) * 100
    r2 = r2_score(y_true, y_pred)

    return {
        "Model": model_name,
        "MAE": round(mae, 3),
        "MSE": round(mse, 3),
        "RMSE": round(rmse, 3),
        "MAPE (%)": round(mape, 3),
        "R2": round(r2, 4),
        "Train Time (s)": round(train_time, 2) if isinstance(train_time, (int, float)) else (train_time if train_time else "-"),
        "Inference Time (s)": round(inference_time, 4) if isinstance(inference_time, (int, float)) else (inference_time if inference_time else "-")
    }

