def add_calendar_features(df, dt_col="datetime"):
    df = df.copy()
    df["month"] = df[dt_col].dt.month
    df["weekday"] = df[dt_col].dt.weekday          # 0=Mon ... 6=Sun
    df["is_weekend"] = (df["weekday"] >= 5).astype(int)
    df["day_of_year"] = df[dt_col].dt.dayofyear

    def get_season(month):
        if month in [12, 1, 2]: return 0   # Winter
        elif month in [3, 4, 5]: return 1  # Summer
        elif month in [6, 7, 8, 9]: return 2  # Monsoon
        else: return 3                      # Post-monsoon/Autumn
    df["season"] = df["month"].apply(get_season)
    return df

