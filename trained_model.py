"""# Cell 20 — Sliding Window Generator (for CNN-LSTM-GRU on Hourly Data)"""

def create_sequences(X, y, window_size=24):
    """
    X: 2D array (n_samples, n_features), already scaled
    y: 2D array (n_samples, 1), already scaled
    window_size: how many past timesteps to look back
    Returns X_seq: (n_samples - window_size, window_size, n_features)
            y_seq: (n_samples - window_size,)
    """
    X_seq, y_seq = [], []
    for i in range(window_size, len(X)):
        X_seq.append(X[i - window_size:i])
        y_seq.append(y[i])
    return np.array(X_seq), np.array(y_seq)

