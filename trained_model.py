def get_aqi_bucket(aqi_value):
    """
    Categorizes the numeric AQI into the official CPCB Indian AQI standard buckets.
    """
    if aqi_value <= 50:
        return "Good"
    elif aqi_value <= 100:
        return "Satisfactory"
    elif aqi_value <= 200:
        return "Moderate"
    elif aqi_value <= 300:
        return "Poor"
    elif aqi_value <= 400:
        return "Very Poor"
    else:
        return "Severe"

def predict_future_date_dynamic(target_date_str):
    """
    Predicts a unique dynamic AQI, individual pollutant levels, and the corresponding
    AQI category bucket for any future day past 2020.
    """
    try:
        target_date = pd.to_datetime(target_date_str)
        if target_date.year > 2100 or target_date.year < 2000:
            return "❌ Input Error: Year out of plausible bounds. Please use the YYYY-MM-DD format (e.g., 2026-07-08)."
    except Exception:
        return "❌ DateParseError: Invalid date format. Please format your entry strictly as YYYY-MM-DD."

    df_temp = df_model.copy()

    # Safely extract Date column
    if 'Date' not in df_temp.columns and 'date' not in df_temp.columns:
        df_temp = df_temp.reset_index()
    for col in df_temp.columns:
        if 'date' in str(col).lower():
            df_temp.rename(columns={col: 'Date'}, inplace=True)
            break

    df_temp['Date'] = pd.to_datetime(df_temp['Date'])

    feature_cols = ['PM2.5', 'PM10', 'NO', 'NO2', 'NOx', 'NH3', 'CO', 'SO2', 'O3']
    target_col = 'AQI'
    local_profile = df_temp.groupby(['Month', 'Day'])[feature_cols].mean().reset_index()

    last_historical_date = df_temp['Date'].max()

    # -------- CASE 1: Date exists in historical data --------
    if target_date <= last_historical_date:
        match = df_temp[df_temp['Date'].dt.date == target_date.date()]
        if not match.empty:
            actual_aqi = match['AQI'].values[0]
            out_str = f"Historical Date Found! Actual AQI on {target_date_str}: {actual_aqi:.2f} ({get_aqi_bucket(actual_aqi)})\n"
            out_str += "=========================================\n"
            out_str += "📊 RECORDED POLLUTANT VALUES:\n"
            out_str += "=========================================\n"
            for col in feature_cols:
                out_str += f" -> {col:7}: {match[col].values[0]:.2f}\n"
            return out_str
        return "Date falls within dataset limits but has missing entries."

    # -------- CASE 2: True Future Forecasting --------
    days_to_forecast = (target_date - last_historical_date).days
    print(f"Projecting {days_to_forecast} days forward beyond {last_historical_date.strftime('%Y-%m-%d')}...")

    current_window = df_scaled_X[-LOOKBACK_WINDOW:].copy()
    latest_predicted_aqi = None
    final_pollutants_scaled = None

    for step in range(days_to_forecast):
        current_step_date = last_historical_date + pd.Timedelta(days=step + 1)
        m = current_step_date.month
        d = current_step_date.day

        input_window = current_window.reshape(1, LOOKBACK_WINDOW, -1)
        deep_features = feature_extractor_model(input_window, training=False).numpy()

        current_time_feats = np.array([[
            m, d, current_step_date.dayofweek, current_step_date.dayofyear, current_step_date.year
        ]])

        xgb_input = np.hstack([deep_features, current_time_feats])

        scaled_pred_aqi = xgb_model.predict(xgb_input)[0]

        # Wrapped as a pandas DataFrame with matched feature names to satisfy the scaler
        pred_df = pd.DataFrame([[scaled_pred_aqi]], columns=[target_col])
        latest_predicted_aqi = scaler_y.inverse_transform(pred_df)[0][0]

        profile_row = local_profile[(local_profile['Month'] == m) & (local_profile['Day'] == d)]
        if not profile_row.empty:
            raw_pollutants = profile_row[feature_cols].copy()
        else:
            raw_pollutants = pd.DataFrame([local_profile[feature_cols].mean().values], columns=feature_cols)

        final_pollutants_scaled = scaler_X.transform(raw_pollutants)
        current_window = np.vstack([current_window[1:], final_pollutants_scaled[0]])

    # Wrapped matching feature name format cleanly
    pollutants_df = pd.DataFrame(final_pollutants_scaled, columns=feature_cols)
    pollutants_unscaled = scaler_X.inverse_transform(pollutants_df)[0]

    # Build clean layout output strings (No emojis to prevent Windows console charmap encoding errors)
    result_string = f"Predicted Future AQI for [{target_date_str}]: {latest_predicted_aqi:.2f}\n"
    result_string += f"Air Quality Category Bucket: {get_aqi_bucket(latest_predicted_aqi)}\n"
    result_string += "=========================================\n"
    result_string += "ESTIMATED INDIVIDUAL POLLUTANT VALUES:\n"
    result_string += "=========================================\n"
    for name, val in zip(feature_cols, pollutants_unscaled):
        result_string += f" -> {name:7}: {val:.2f}\n"

    return result_string

