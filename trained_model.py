# IQR-based outlier counts
def iqr_outlier_report(df, cols):
    rows = []
    for col in cols:
        Q1, Q3 = df[col].quantile([0.25, 0.75])
        IQR = Q3 - Q1
        lower, upper = Q1 - 1.5 * IQR, Q3 + 1.5 * IQR
        n_outliers = ((df[col] < lower) | (df[col] > upper)).sum()
        rows.append([col, lower, upper, n_outliers])
    return pd.DataFrame(rows, columns=["column", "lower_bound", "upper_bound", "n_outliers"])

print(iqr_outlier_report(daily, pollutant_cols))

"""# Cell 9 — Outlier Treatment (Capping, not deletion)"""

def cap_outliers(df, cols):
    df = df.copy()
    for col in cols:
        Q1, Q3 = df[col].quantile([0.25, 0.75])
        IQR = Q3 - Q1
        lower, upper = Q1 - 1.5 * IQR, Q3 + 1.5 * IQR
        df[col] = df[col].clip(lower=lower, upper=upper)
    return df

