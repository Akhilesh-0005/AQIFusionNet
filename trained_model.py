"""# Cell 53 — Predict AQI for a User-Given Date"""

def predict_aqi_for_date(date_str):
    target_date = pd.to_datetime(date_str)
    last_known_date = daily_full["datetime"].max()

    # -------- CASE 1: Date exists in historical data --------
    if target_date <= last_known_date:
        row = daily_full[daily_full["datetime"] == target_date]
        if row.empty:
            print(f"No record found for {date_str} (check date format / range: "
                  f"{daily_full['datetime'].min().date()} to {last_known_date.date()})")
            return None

        X_row = row[daily_feature_cols]
        X_scaled = daily_scaler_X.transform(X_row)
        pred_scaled = final_xgb_model.predict(X_scaled).reshape(-1, 1)
        pred_aqi = daily_scaler_y.inverse_transform(pred_scaled)[0][0]
        actual_aqi = row["AQI"].values[0]

        print(f"Date: {date_str}  (historical)")
        print(f"Predicted AQI: {pred_aqi:.2f}")
        print(f"Actual AQI:    {actual_aqi:.2f}")
        print(f"Absolute Error: {abs(pred_aqi - actual_aqi):.2f}")
        return pred_aqi

    # -------- CASE 2: Future date beyond dataset --------
    else:
        print(f"Date: {date_str}  (future — beyond last recorded date {last_known_date.date()})")
        print("NOTE: Future pollutant levels are unknown. Using the historical seasonal "
              "average for that calendar month as a proxy — treat this as an approximation, "
              "not a true forecast, and state this limitation in your report.\n")

        days_ahead = (target_date - last_known_date).days
        history = daily_full.copy()

        # Recursive day-by-day forecasting
        for step in range(1, days_ahead + 1):
            next_date = last_known_date + pd.Timedelta(days=step)
            month = next_date.month

            # Approximate pollutant features using historical same-month average
            month_avg = history[history["datetime"].dt.month == month][pollutant_cols].mean()

            new_row = {col: month_avg[col] for col in pollutant_cols}
            new_row["datetime"] = next_date
            new_row["month"] = month
            new_row["weekday"] = next_date.weekday()
            new_row["is_weekend"] = int(next_date.weekday() >= 5)
            new_row["day_of_year"] = next_date.dayofyear
            new_row["season"] = (0 if month in [12,1,2] else 1 if month in [3,4,5]
                                  else 2 if month in [6,7,8,9] else 3)

            # Lag features come from actual/predicted AQI history
            for lag in [1, 2, 3, 7, 14, 30]:
                lag_date = next_date - pd.Timedelta(days=lag)
                match = history[history["datetime"] == lag_date]
                new_row[f"AQI_lag{lag}"] = match["AQI"].values[0] if not match.empty else history["AQI"].mean()

            # Rolling features from recent history
            for w in [3, 7, 14]:
                recent = history[history["datetime"] < next_date].tail(w)["AQI"]
                new_row[f"AQI_rollmean{w}"] = recent.mean()
